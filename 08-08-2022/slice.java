/*
 * A simple java class which is executing the Slicing concept in an  * Array to reverse the char array's last 5 elements
 * @Ajaisharma 
 */
class Slice {
    public static void main(String[] args) {
	char[] name = {'a','j','a','i','s','h','a','r','m','a'};
	int arrayLength = name.length;
	for(int iterator = arrayLength - 1; i > arrayLength - 6; i--) {
	    System.out.println(name[iterator]);
	}
    }
}	
	